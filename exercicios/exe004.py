entrada = input('Informe alguma coisa: ')
print ('É SÓ ESPAÇO: {}'.format(entrada.isspace()))
print ('TIPO DO INPUT: {}'.format(type(entrada)))
print ('É UM NÚMERO: {}'.format(entrada.isnumeric()))
print ('É UMA STRING: {}'.format(entrada.isalpha()))
print ('É UM ALFANUMERICO: {}'.format(entrada.isalnum()))
print ('ESTÁ EM MAIUSCULO: {}'.format(entrada.isupper()))
print ('ESTÁ EM MINUSCULO: {}'.format(entrada.islower()))
print ('ESTÁ CAMECASE: {}'.format(entrada.istitle()))



